class LanguageSystem {
    constructor() {
        this.currentLang = localStorage.getItem('sirius_lang') || 'tr';
        this.langButtons = document.querySelectorAll('.lang-btn');
        this.init();
    }

    init() {
        this.updateUI(this.currentLang);
        this.bindEvents();
    }

    bindEvents() {
        this.langButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.id.replace('lang-', '');
                if (translations[lang] && Object.keys(translations[lang]).length > 0) {
                    this.switchLanguage(lang);
                }
            });
        });
    }

    switchLanguage(lang) {
        if (lang === this.currentLang) return;

        this.currentLang = lang;
        localStorage.setItem('sirius_lang', lang);

        // Dynamic transitions
        document.body.style.opacity = '0';

        setTimeout(() => {
            this.updateUI(lang);
            this.handleRTL(lang);
            document.body.style.opacity = '1';
        }, 150);
    }

    updateUI(lang) {
        const langData = translations[lang];

        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (langData[key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = langData[key];
                } else {
                    element.textContent = langData[key];
                }
            }
        });

        // Update placeholders specifically
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (langData[key]) element.placeholder = langData[key];
        });

        // Update titles/aria-labels
        document.querySelectorAll('[aria-label][data-i18n-label]').forEach(element => {
            const key = element.getAttribute('data-i18n-label');
            if (langData[key]) element.setAttribute('aria-label', langData[key]);
        });

        document.querySelectorAll('[title][data-i18n-title]').forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            if (langData[key]) element.title = langData[key];
        });

        // Update active class on buttons
        this.langButtons.forEach(btn => {
            btn.classList.toggle('active', btn.id === `lang-${lang}`);
        });

        // Update document lang attribute
        document.documentElement.lang = lang;
    }

    handleRTL(lang) {
        if (lang === 'ar') {
            document.body.dir = 'rtl';
            document.body.classList.add('rtl-mode');
        } else {
            document.body.dir = 'ltr';
            document.body.classList.remove('rtl-mode');
        }
    }
}

class ChatSystem {
    constructor() {
        this.panel = document.getElementById('chat-panel');
        this.toggle = document.getElementById('chat-toggle');
        this.close = document.getElementById('chat-close');
        this.input = document.getElementById('chat-input');
        this.sendBtn = document.getElementById('chat-send');
        this.messages = document.getElementById('chat-messages');

        if (this.panel && this.toggle) {
            this.init();
        }
    }

    init() {
        // Toggle Panel
        this.toggle.addEventListener('click', (e) => {
            e.stopPropagation();
            this.panel.classList.toggle('active');
            if (this.panel.classList.contains('active')) {
                this.input.focus();
            }
        });

        // Close Panel
        this.close.addEventListener('click', () => {
            this.panel.classList.remove('active');
        });

        // Click Outside to Close
        document.addEventListener('click', (e) => {
            if (!this.panel.contains(e.target) && !this.toggle.contains(e.target)) {
                this.panel.classList.remove('active');
            }
        });

        // Send Message
        this.sendBtn.addEventListener('click', () => this.sendMessage());
        this.input.addEventListener('keypress', (e) => {
            if (e.key === 'Enter') this.sendMessage();
        });
    }

    sendMessage() {
        const text = this.input.value.trim();
        if (!text) return;

        // Add User Message
        this.addMessage(text, 'user-message');
        this.input.value = '';

        // Simulate Bot Response
        setTimeout(() => {
            const lang = localStorage.getItem('sirius_lang') || 'tr';
            const response = translations[lang].chatBotResponse || "...";
            this.addMessage(response, 'system-message');
        }, 1000);
    }

    addMessage(text, type) {
        const msgDiv = document.createElement('div');
        msgDiv.className = `message ${type}`;
        msgDiv.innerHTML = `<p>${text}</p>`;
        this.messages.appendChild(msgDiv);

        // Scroll to bottom
        this.messages.scrollTop = this.messages.scrollHeight;
    }
}

// Initialize on DOM load
document.addEventListener('DOMContentLoaded', () => {
    // Start Language System
    new LanguageSystem();

    // Start Chat System
    new ChatSystem();

    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });

    // Update Year
    const yearElement = document.getElementById('current-year');
    if (yearElement) {
        yearElement.textContent = new Date().getFullYear();
    }

    // Modal Logic
    const modal = document.getElementById("analysisModal");
    const modalTriggers = document.querySelectorAll(".btn-modal-trigger, .btn-gold-gradient[href='#iletisim'], .btn-outline[href='index.html#iletisim'], .open-analysis-modal");
    const closeBtn = document.querySelector(".modal-close");
    const sectorSelect = document.getElementById("sectorSelect");
    const otherSector = document.getElementById("otherSector");

    if (modal && closeBtn) {
        modalTriggers.forEach(trigger => {
            trigger.addEventListener("click", function (e) {
                e.preventDefault();
                modal.classList.add("show");
            });
        });

        closeBtn.addEventListener("click", function () {
            modal.classList.remove("show");
        });

        window.addEventListener("click", function (e) {
            if (e.target === modal) {
                modal.classList.remove("show");
            }
        });

        if (sectorSelect && otherSector) {
            sectorSelect.addEventListener("change", function () {
                if (this.value === "other") {
                    otherSector.style.display = "block";
                } else {
                    otherSector.style.display = "none";
                }
            });
        }

        const form = modal.querySelector("form");
        if (form) {
            form.addEventListener("submit", function (e) {
                e.preventDefault();

                // Validate at least one service checkbox is checked
                const serviceCheckboxes = form.querySelectorAll('input[name="services"]');
                if (serviceCheckboxes.length > 0) {
                    let isChecked = false;
                    serviceCheckboxes.forEach(cb => {
                        if (cb.checked) isChecked = true;
                    });
                    if (!isChecked) {
                        alert("Lütfen en az bir hizmet seçiniz.");
                        return;
                    }
                }

                const submitBtn = form.querySelector("button[type='submit']");
                if (submitBtn) {
                    submitBtn.disabled = true;
                    submitBtn.innerText = "Gönderiliyor...";
                }

                // Create success box
                const successMessage = document.createElement("div");
                successMessage.innerHTML = "Talebiniz alınmıştır. Ekibimiz en kısa sürede sizinle iletişime geçecektir.";

                successMessage.style.marginTop = "20px";
                successMessage.style.padding = "15px";
                successMessage.style.borderRadius = "8px";
                successMessage.style.background = "rgba(34,197,94,0.1)";
                successMessage.style.border = "1px solid rgba(34,197,94,0.4)";
                successMessage.style.color = "#22c55e";
                successMessage.style.fontWeight = "500";
                successMessage.style.opacity = "0";
                successMessage.style.transition = "opacity 0.3s ease";

                form.appendChild(successMessage);

                // Fade in
                setTimeout(() => {
                    successMessage.style.opacity = "1";
                }, 50);

                if (submitBtn) {
                    submitBtn.innerText = "Gönderildi ✓";
                }

                // Auto close after 2 seconds
                setTimeout(() => {
                    modal.classList.remove("show");
                    form.reset();
                    successMessage.remove();

                    if (submitBtn) {
                        submitBtn.disabled = false;
                        submitBtn.innerText = "Gönder";
                    }
                }, 2000);
            });
        }
    }

    // ESC to close modal
    document.addEventListener("keydown", function (e) {
        if (modal && e.key === "Escape" && modal.classList.contains("show")) {
            modal.classList.remove("show");
        }
    });

    // Accordion Logic
    const accordionItems = document.querySelectorAll(".accordion-item");

    accordionItems.forEach(item => {
        const header = item.querySelector(".accordion-header");
        if (header) {
            header.addEventListener("click", function (e) {
                // If the click was on the modal button inside the body, don't toggle
                if (e.target.closest('.btn-modal-trigger')) return;

                const isActive = item.classList.contains("active");

                if (!isActive) {
                    item.classList.add("active");
                } else {
                    item.classList.remove("active");
                }
            });
        }
    });

    // FAQ Accordion Logic
    const faqItems = document.querySelectorAll(".faq-item");

    faqItems.forEach(item => {
        const questionBtn = item.querySelector(".faq-question");
        if (questionBtn) {
            questionBtn.addEventListener("click", () => {
                const isActive = item.classList.contains("active");

                // Close all other FAQs
                faqItems.forEach(faq => faq.classList.remove("active"));

                // Open clicked FAQ if it wasn't already active
                if (!isActive) {
                    item.classList.add("active");
                }
            });
        }
    });

    // Multi-Select Dropdown Logic
    const multiSelectToggle = document.querySelector(".multi-select-toggle");
    const multiSelect = document.querySelector(".multi-select");

    if (multiSelectToggle && multiSelect) {
        multiSelectToggle.addEventListener("click", function (e) {
            e.stopPropagation();
            multiSelect.classList.toggle("open");
        });

        // Close dropdown when clicking outside
        document.addEventListener("click", function (e) {
            if (!multiSelect.contains(e.target)) {
                multiSelect.classList.remove("open");
            }
        });
    }
});